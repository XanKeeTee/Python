"""
4. Escribe un programa que recoja tres números y calcule su media aritmética.
"""

num1 = float(input("Dame el primer numero: "))
num2 = float(input("Dame el segundo numero: "))
num3 = float(input("Dame el tercer numero: "))
media = (num1 + num2 + num3) / 3
print("La media aritmetica de los 3 numeros es de: ", media)