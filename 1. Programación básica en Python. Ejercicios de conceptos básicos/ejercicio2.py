"""
2. Escribe un programa que recoja dos números enteros por teclado y muestre
los siguientes resultados: suma, resta, multiplicación, división real, división
entera, resto de la división entera y potencia.
"""

valor1 = int(input("Dame el primer valor: "))
valor2 = int(input("Dame el segundo valor: "))

print("La suma de los 2 valores es de",valor1+valor2)
print("La resta de los 2 valores es de",valor1-valor2)
print("La multiplicacion de los 2 valores es de",valor1*valor2)
print("La division real de los 2 valores es de",valor1/valor2)
print("La division entera de los 2 valores es de",valor1//valor2)
print("El resto de la division entera de los 2 valores es de",valor1%valor2)
print("La potencia de los 2 valores es de",valor1**valor2)